package pack1;

public class Calculator {
    /**
     * Operace scitani
     * @param a prvni cislo
     * @param b druhe cislo
     * @return vraci soucet
     */
    public int Add(int a, int b){
        return a+b;
    }

    /**
     * Operace odcitani
     * @param a prvni cislo
     * @param b druhe cislo
     * @return vraci rozdil cisel
     */
    public int Sub(int a, int b){
        return a-b;
    }
}
